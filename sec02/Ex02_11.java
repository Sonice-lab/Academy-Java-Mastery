package sec02;

public class Ex02_11 {
    public static void main(String[] args) {
        // 정수형
        byte num1 = 20; // 메모리 1byte 사용
        System.out.println(num1);

        // ctrl + d : 동일 키워드에 커서 추가 생성(esc 키로 원복)
        short num2 = 20; // 2byte
        System.out.println(num2);

        int num3 = 20; // 4byte
        System.out.println(num3);

        long num4 = 20; // 8byte(자바에서 가장 큰(작은) 정수 표현 가능)
        System.out.println(num4);

    
    }
}
