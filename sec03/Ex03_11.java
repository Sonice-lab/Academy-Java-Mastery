package sec03;

public class Ex03_11 {
    public static void main(String[] args) {
        int num = 1;
        System.out.println(num);

        //num = num + 1; = num += 1;
        num += 1; // 복합 대입 연산자
        System.out.println(num);

        //num = num*2;
        num *= 2;  //모든 산술 연산자와 복합 사용 가능
        System.out.println(num);



    }
}
