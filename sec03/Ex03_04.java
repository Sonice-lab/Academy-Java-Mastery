package sec03;

public class Ex03_04 {
    public static void main(String[] args) {
        int num = 1;
        System.out.println(num);
        // 순서가 앞뒤여도 결과는 같다.
        ++num;
        System.out.println(num);
        ++num;
        System.out.println(num);
        
        --num;
        System.out.println(num);

        --num;
        System.out.println(num);



    }
}
