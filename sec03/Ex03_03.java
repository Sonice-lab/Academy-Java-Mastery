package sec03;

public class Ex03_03 {
    public static void main(String[] args) {
        int num = 1;
        System.out.println(num);
        num++; //기존의 수에서 1씩 증가함 > 증감 연산자 > 증가 연산자
        System.out.println(num);

        num++;
        System.out.println(num);

        num--; //감소 연산자
        System.out.println(num);
        
        num--; //감소 연산자
        System.out.println(num);

    }
}
