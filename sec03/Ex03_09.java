package sec03;

public class Ex03_09 {
    public static void main(String[] args) {
        // 이항 연산자
        // 산술 연산자
        System.out.println(1+1);
        System.out.println(1-1);
        System.out.println(2*2);
        System.out.println(4/2);
        System.out.println(5/2); //소수점 유실
        System.out.println(5/2f); // f를 붙여 유실 막기
        System.out.println(5%2); //나눗셈의 나머지를 구하는 연산자
    }
}
