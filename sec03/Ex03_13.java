package sec03;

public class Ex03_13 {
    public static void main(String[] args) {
        // 비교 연산자
        //  평가 결과는 boolean
        System.out.println(1<2);
        System.out.println(1>2);
        System.out.println(1==2); //=는 할당 연산자
        System.out.println(2==2); 
        System.out.println(1<=2); 
        System.out.println(2>=1); 
        System.out.println(1!=2); //같지 않음을 의미

    }
}
