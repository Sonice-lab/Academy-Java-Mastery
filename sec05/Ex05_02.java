package sec05;

public class Ex05_02 {
    public static void main(String[] args) {
        System.out.println("--- 카운트다운 ---");
        // for문 초기화식 변수명은 관례적으로 i(index)를 사용함
        for(int i = 10 ; i > 0 ;i--){
            System.out.println(i);
        }

        System.out.println("로켓 발사~");
       
    }
}
