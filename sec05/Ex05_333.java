package sec05;

public class Ex05_333 {
    public static void main(String[] args) {
        // \t를 사용하지 않았을 때 (삐뚤빼뚤할 수 있음)
        System.out.println("이름:홍길동 나이:20");

        // \t를 사용했을 때 (일정한 간격으로 정렬됨)
        System.out.println("이름:홍길동\t나이:20\t지역:부천");
        System.out.println("이름:이순신\t나이:40\t지역:서울");
    }
}